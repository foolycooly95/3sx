void effect_J7_move(WORK_Other* ewk) {
    PLW* mwk = (PLW*)ewk->my_master;

    ewk->wu.rl_flag = mwk->wu.rl_flag;

    switch (ewk->wu.routine_no[0]) {
    case 0:
        if (mwk->gill_ccch_go == 0 && (mwk->wu.routine_no[1] != 0 || mwk->wu.routine_no[2] != 1)) {
            break;
        }

        ewk->wu.routine_no[0]++;
        ewk->wu.hit_adrs = pl00_cctbl[ewk->wu.type][0];
        ewk->wu.dmg_adrs = pl00_cctbl[ewk->wu.type][1];
        ewk->wu.step_xy_table = (s16*)ColorRAM[ewk->master_id * 16];
        ewk->wu.move_xy_table = (s16*)ColorRAM[(ewk->master_id * 16) + 8];
        ewk->wu.dir_timer = 0;
        ewk->wu.dir_step = 0;
        ewk->wu.dir_old = 1;
        check_new_color_data(&ewk->wu);
        break;

    case 1:
        if (ewk->wu.dead_f == 1) {
            ewk->wu.routine_no[0] = 3;
        } else if (mwk->wu.vital_new < 0) {
            ewk->wu.routine_no[0] = 2;
            ewk->wu.routine_no[1] = 0;
        } else if (EXE_flag == 0 && Game_pause == 0) {
            check_new_color_data(&ewk->wu);
        }

        break;

    case 2:
        switch (ewk->wu.routine_no[1]) {
        case 0:
            ewk->wu.routine_no[1]++;
            ewk->wu.dir_timer = 0;
            ewk->wu.dir_step = 0;
            ewk->wu.dir_old = 1;

            if (ewk->wu.rl_flag) {
                get_new_color_data(&ewk->wu, (ColorCode*)ewk->wu.hit_adrs, ewk->wu.step_xy_table);
                get_new_color_data(&ewk->wu, (ColorCode*)ewk->wu.dmg_adrs, ewk->wu.move_xy_table);
            } else {
                get_new_color_data(&ewk->wu, (ColorCode*)ewk->wu.dmg_adrs, ewk->wu.move_xy_table);
                get_new_color_data(&ewk->wu, (ColorCode*)ewk->wu.hit_adrs, ewk->wu.step_xy_table);
            }

            break;

        case 1:
            if (mwk->wu.routine_no[1] == 4 && mwk->wu.routine_no[2] == 21) {
                ewk->wu.routine_no[1]++;
            }

            break;

        case 2:
            if (mwk->wu.routine_no[1] != 4 || mwk->wu.routine_no[2] != 21) {
                ewk->wu.routine_no[0] = 1;
                ewk->wu.routine_no[1] = 0;
            }

            break;
        }

        break;

    default:
        push_effect_work(&ewk->wu);
        break;
    }
}

void check_new_color_data(WORK* wk) {
    if (--wk->dir_timer >= 0) {
        return;
    }

    if (wk->dir_old) {
        wk->dir_step = 1;
    } else {
        wk->dir_step++;
    }

    if (wk->rl_flag) {
        get_new_color_data(wk, (ColorCode*)wk->hit_adrs, wk->step_xy_table);
        get_new_color_data(wk, (ColorCode*)wk->dmg_adrs, wk->move_xy_table);
    } else {
        get_new_color_data(wk, (ColorCode*)wk->dmg_adrs, wk->move_xy_table);
        get_new_color_data(wk, (ColorCode*)wk->hit_adrs, wk->step_xy_table);
    }
}

void get_new_color_data(WORK* wk, ColorCode* trom, s16* tram) {
    const s16* data;
    s16 i;
    u16 col;

    wk->dir_timer = trom[wk->dir_step].timer;
    wk->dir_old = trom[wk->dir_step].endcode;
    data = trom[wk->dir_step].adrs;

    for (i = 0; i < 48; i++) {
        col = *data++;
        *tram++ = palConvSrcToRam(col);
    }
}

s32 effect_J7_init(PLW* wk) {
    WORK_Other* ewk;
    s16 ix;

    if (wk->player_number != 0) {
        return 0;
    }

    if (My_char[wk->wu.id] != 0) {
        return 0;
    }

    if ((ix = pull_effect_work(6)) == -1) {
        return -1;
    }

    ewk = (WORK_Other*)frw[ix];
    ewk->wu.be_flag = 1;
    ewk->wu.id = 197;
    ewk->wu.work_id = 16;
    ewk->wu.type = Player_Color[wk->wu.id];
    ewk->my_master = wk;
    ewk->master_id = wk->wu.id;
    ewk->master_work_id = wk->wu.work_id;
    ewk->master_player = wk->player_number;
    return 0;
}

