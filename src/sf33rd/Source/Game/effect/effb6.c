void get_message_conn_data(WORK_Other_CONN* ewk, s16 kind, s16 pl, s16 msg) {
    u8** msghead;
    u8* msgtbl;
    s32 msgline;
    s32 slideX;
    s32 slideY;
    s32 currX;
    s32 currY;
    s32 i;
    s32 hzsel;
    s32 space;
    s32 hzflag;
    s32 bytes;
    s32 mjcnt;
    u16 objnum;

    s32 assign;

    currX = 0;
    currY = 0;
    mjcnt = 0;
    slideX = 8;
    assign = -16;
    slideY = assign;
    hzsel = 0;

    if (msg >= 0xFF) {
        return;
    }

    msghead = (u8**)mess_tables[kind][pl]->msgAdr[msg];
    msgline = mess_tables[kind][pl]->msgNum[msg];

    for (i = 0; i < msgline; i++) {
        for (msgtbl = msghead[i]; *msgtbl; msgtbl += bytes) {
            bytes = msgConvertObjNum(msgtbl, &space, &hzflag, &objnum, hzsel);

            if (!space) {
                ewk->conn[mjcnt].nx = currX;
                ewk->conn[mjcnt].ny = currY;
                ewk->conn[mjcnt].col = 0;
                ewk->conn[mjcnt].chr = objnum;
                mjcnt++;

                if (mjcnt > 108) {
                    while (1) {
                        // do nothing
                    }
                }
            }

            if (hzflag) {
                currX += slideX * 2;
            } else {
                currX += slideX;
            }
        }

        currX = 0;
        currY += slideY;
    }

    ewk->num_of_conn = mjcnt;
}

s32 msgConvertObjNum(u8* moji, s32* spc, s32* hz, u16* num, u8 hzSel) {
    u8 tmpstr[4];
    s32 i;
    s32 j;
    s32 rnum;

    if (hzSel != 0) {
        tmpstr[0] = moji[0];
        tmpstr[1] = moji[1];
        tmpstr[2] = 0;

        if (tmpstr[0] == ' ') {
            goto one;
        }

        if (strcmp(&tmpstr[0], "\x62\x68") == 0) {
            goto two;
        }

        if (strcmp(&tmpstr[0], "\x81\x40") == 0 || strcmp(&tmpstr[0], "\x81\x51") == 0) {
            goto three;
        }

        if (msgCheckCodeSize(tmpstr[0]) == 1) {
            tmpstr[1] = ((u8**)src_han_zen_conv)[tmpstr[0]][1];
            tmpstr[0] = ((u8**)src_han_zen_conv)[tmpstr[0]][0];

            for (i = 0; i < 3; i++) {
                for (j = 0; j < 128; j++) {
                    if (strcmp(&tmpstr[0], han_adrs[i][j]) != 0) {
                        continue;
                    }

                    *hz = 0;
                    *spc = 0;
                    *num = j + (i * 128) + 0x7F30;
                    return 1;
                }
            }
        }

        for (i = 0; i < 11; i++) {
            for (j = 0; j < 128; j++) {
                if (strcmp(&tmpstr[0], zen_adrs[i][j]) != 0) {
                    continue;
                }

                *hz = 1;
                *spc = 0;
                *num = j + (i * 128) + 0x80B0;
                return 2;
            }
        }

        goto three;
    }

    if (moji[0] == 0x5E) {
        tmpstr[0] = moji[0];
        tmpstr[1] = moji[1];
        tmpstr[2] = 0;
        rnum = 2;
    } else {
        tmpstr[0] = moji[0];
        tmpstr[1] = 0;
        rnum = 1;
    }

    if (tmpstr[0] == ' ') {
        goto one;
    }

    for (i = 0; i < 2; i++) {
        for (j = 0; j < 128; j++) {
            if (strcmp(&tmpstr[0], han_adrs[i][j]) != 0) {
                continue;
            }

            *hz = 0;
            *spc = 0;
            *num = j + (i * 128) + 0x7F30;
            return rnum;
        }
    }

one:
    *hz = 0;
    *spc = 1;
    *num = 0;
    return 1;

two:
    *hz = 0;
    *spc = 1;
    *num = 0;
    return 2;

three:
    *hz = 1;
    *spc = 1;
    *num = 0;
    return 2;
}

s32 msgCheckCodeSize(u8 data) {
    if (data >= 128 && data < 160) {
        return 2;
    }

    if (data >= 224) {
        return 2;
    }

    return 1;
}
